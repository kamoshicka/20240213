'use strict'

// 1. 3つ以上の値を⽤意して変数に代⼊してください。その1番⽬と3番⽬をコンソールで表示
var object = [10,20,30];
console.log(object[0],object[2]);

// 2. key、value、プロパティーについて説明してください。
// key.　プロパティの名前にあたる箇所。自由に設定することができる文字列であり、ダブルクォーテーションは不要。
// value.　プロパティの値にあたる箇所。数値、文字列がここに入る。
// プロパティ.　変数に対して代入した値に名前を付けたもの。

// 3. 3つほどプロパティを⽤意したオブジェクトを⽤意してコンソールで表⽰
var object = [
    {
        time:"20seconds",
        score:23400,
        rank: 2,
    }
]
console.log(object);

// 4. オブジェクトを3つ⽤意してそれを配列として代⼊してください。配列の0番⽬と3番⽬を表⽰させ、その中のkeyを指定してコンソールに表⽰
var object = [
    {
        time:"20seconds",
        score:23400,
        rank: 2,
    },
    {
        time:"35seconds",
        score:20000,
        rank: 3,
    },
    {
        time:"17seconds",
        score:25100,
        rank: 1,
    }
]
console.log(object[0],object[2]);